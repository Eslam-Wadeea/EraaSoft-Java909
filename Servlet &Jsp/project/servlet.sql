CREATE TABLE ITEM(ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY 
, NAME VARCHAR2(255) UNIQUE  
, PRICE NUMBER(6,2) ,
TOTAL_NUMBER NUMBER(5) ,
IS_DELETED NUMBER,
PRIMARY KEY(ID));

