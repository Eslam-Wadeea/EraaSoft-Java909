CREATE TABLE ITEM_DETAILS (
    ID NUMBER PRIMARY KEY,
    DESCRIPTION VARCHAR2(500),
    ISSUE_DATE DATE DEFAULT SYSDATE,
    EXPIRY_DATE DATE,
    ITEM_ID NUMBER UNIQUE, 
    CONSTRAINT FK_ITEM_DETAILS FOREIGN KEY (ITEM_ID) REFERENCES ITEM(ID)
);

